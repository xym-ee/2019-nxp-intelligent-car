static void oled_data(uint8_t data)
{
  unsigned char i = 8;
  LCD_DC(1);
  LCD_SCL(0);
  delayus(1);    
  while(i--)
  {
    if(data&0x80)
      LCD_SDA(1);
    else
      LCD_SDA(0);
    LCD_SCL(1); 
    delayus(1);          
    LCD_SCL(0);    
    data<<=1;    
  }
}

void LCD_WrCmd(unsigned char cmd)
{
  unsigned char i=8;
  LCD_DC(0);
  LCD_SCL(0);
  delayus(1);	  
  while(i--)
  {
    if(cmd&0x80)
      LCD_SDA(1);
    else
      LCD_SDA(0);
    LCD_SCL(1);
    delayus(1);	            
    LCD_SCL(0);    
    cmd<<=1;;   
  }
}

void LCD_Set_Pos(unsigned char x, unsigned char y)
{ 
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)); 
}

static void oled_clear(void)
{
  unsigned char y,x;	
  for(y=0;y<8;y++)
  {
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(0x01);
    LCD_WrCmd(0x10); 
    for(x=0;x<OLED_WIDTH;x++)
      oled.ops->data(0);
  }
}

